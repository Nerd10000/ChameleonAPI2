package dragon.me.chameleonAPI2.math;

public class Vector3Axis {

    private double x,y,z;
    /**
     * @param x X-axis on a codinate system
     * @param y Y-axis on a codinate system
     * @param z Z axis on a codinate system
     */
    public  Vector3Axis(double x,double y,double z){
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
